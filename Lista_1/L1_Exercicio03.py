# Entrada de dados (leitura de valores
x = int(input('Valor Inteiro 1? '))
y = int(input('Valor Inteiro 2? '))

# Saída de dados (impressão)
z = x + y
print('x + y =', z)

z = x - y
print('x - y =', z)

print('x * y =', x * y)
print('x / y =', x / y)
print('x // y =', x // y)
print('x ** y =', x**y)
