# Entrada de dados: lê um número e guarda na variável a
a = input('Digite um numero: ')

# Entrada de dados: lê outro número e guarda na variável b
b = input('Digite outro numero: ')

# Saida de dados: exibe variáveis a e b
print(a)
print(b)
print("a =", a)
print("b =", b)

# Saida de dados: exibe o tipo (de dados) das variáveis a e b
print("tipo a =", type(a))
print("tipo b =", type(b))

# Saida de dados: realiza soma
print(a + b)
print("concatena a + b =", a + b)
print(int(a) + int(b))
print("soma a + b =", int(a) + int(b))
