# Entrada de dados: lê um número inteiro e guarda na variável inteiro
inteiro = int(input("Digite um inteiro: "))

# Entrada de dados: lê um número real e guarda na variável real
real = float(input("Digite outro real: "))

# Saida de dados: exibe o tipo (de dados) das variáveis a e b
print("tipo inteiro =", type(inteiro))
print("tipo real =", type(real))

# Saida de dados: exibe resultados de operações com inteiro e real
print("soma:", inteiro + real)
print("multiplicacao:", inteiro * real)
print("subtracao:", inteiro - real)
print("divisao:", inteiro / real)
print("resultado tipo:", type(inteiro + real))
